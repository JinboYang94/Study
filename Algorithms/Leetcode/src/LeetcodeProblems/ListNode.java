package LeetcodeProblems;

/**
 * Definition for singly-linked list.
 * public class LeetcodeProblems.ListNode {
 *     int val;
 *     LeetcodeProblems.ListNode next;
 *     LeetcodeProblems.ListNode(int x) { val = x; }
 * }
 */

public class ListNode {
  public int val;
  public ListNode next;

  public ListNode(int x) {
    val = x;
    next = null;
  }
}